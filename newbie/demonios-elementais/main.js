let type = 'Demônio do fogo'

switch (type){
    case 'Demônio do fogo':
        console.log('A fraqueza do demônio é: água')
        break;
    case 'Demônio do gelo':
        console.log('A fraqueza do demônio é: fogo')
        break;
    case 'Demônio da fumaça':
        console.log('A fraqueza do demônio é: vento')
        break;
    case 'Demônio da escuridão':
        console.log('A fraqueza do demônio é: luz')
        break;
    default:
        console.log('Não foi possível identificar a fraqueza do demônio')
}